// Relational or comparison operators

// > - more than
// < - less than
// >= - more than or equal
// <= - less than or equal

var result = 6 <= 5
console.log(result)

// Equality operators

var x= 1

console.log(x == 1)    // true    // loose comparison: we don't compare the data type
console.log(x === 1)   // true    // strict comparison: we compare the data type as well

console.log(x == '1')  // true   // loose comparison: we don't compare the data type
console.log(x === '1') // false  // strict comparison: we compare the data type as well
